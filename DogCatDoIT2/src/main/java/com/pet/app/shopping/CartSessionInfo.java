package com.pet.app.shopping;

public class CartSessionInfo {
	private int cartCount;
	private int cartTotalItemPrice;
	
	public int getCartCount() {
		return cartCount;
	}
	public void setCartCount(int cartCount) {
		this.cartCount = cartCount;
	}
	public int getCartTotalItemPrice() {
		return cartTotalItemPrice;
	}
	public void setCartTotalItemPrice(int cartTotalItemPrice) {
		this.cartTotalItemPrice = cartTotalItemPrice;
	}
}
