package com.pet.app.shopping;

public class DetailOption {
	private String itemName;
	private long detailId;
	private long itemoptionid;
	private int stock;
	private String detailname;
	private String optionName;
	private String shopStoreName;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public long getDetailId() {
		return detailId;
	}
	public void setDetailId(long detailId) {
		this.detailId = detailId;
	}
	public long getItemoptionid() {
		return itemoptionid;
	}
	public void setItemoptionid(long itemoptionid) {
		this.itemoptionid = itemoptionid;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public String getDetailname() {
		return detailname;
	}
	public void setDetailname(String detailname) {
		this.detailname = detailname;
	}
	public String getOptionName() {
		return optionName;
	}
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	public String getShopStoreName() {
		return shopStoreName;
	}
	public void setShopStoreName(String shopStoreName) {
		this.shopStoreName = shopStoreName;
	}


	
	
	
}
