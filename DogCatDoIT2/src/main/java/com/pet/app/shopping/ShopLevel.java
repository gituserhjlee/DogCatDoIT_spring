package com.pet.app.shopping;

public class ShopLevel {
	private int levelId;
	private String levelName;
	private long moneyStart;
	private long moneyEnd;
	private double rate;
	public int getLevelId() {
		return levelId;
	}
	public void setLevelId(int levelId) {
		this.levelId = levelId;
	}
	public String getLevelName() {
		return levelName;
	}
	public void setLevelName(String levelName) {
		this.levelName = levelName;
	}
	public long getMoneyStart() {
		return moneyStart;
	}
	public void setMoneyStart(long moneyStart) {
		this.moneyStart = moneyStart;
	}
	public long getMoneyEnd() {
		return moneyEnd;
	}
	public void setMoneyEnd(long moneyEnd) {
		this.moneyEnd = moneyEnd;
	}
	public double getRate() {
		return rate;
	}
	public void setRate(double rate) {
		this.rate = rate;
	}
	
	
	
}
