package com.pet.app.shopping;

public class ShopStore {
	private long shopStoreId;
	private String shopStoreName;
	private String shopStoreTel;
	private String shopStoreAddress1;
	private String shopStoreAddress2;
	private boolean enabled;
	public long getShopStoreId() {
		return shopStoreId;
	}
	public void setShopStoreId(long shopStoreId) {
		this.shopStoreId = shopStoreId;
	}
	public String getShopStoreName() {
		return shopStoreName;
	}
	public void setShopStoreName(String shopStoreName) {
		this.shopStoreName = shopStoreName;
	}
	public String getShopStoreTel() {
		return shopStoreTel;
	}
	public void setShopStoreTel(String shopStoreTel) {
		this.shopStoreTel = shopStoreTel;
	}
	public String getShopStoreAddress1() {
		return shopStoreAddress1;
	}
	public void setShopStoreAddress1(String shopStoreAddress1) {
		this.shopStoreAddress1 = shopStoreAddress1;
	}
	public String getShopStoreAddress2() {
		return shopStoreAddress2;
	}
	public void setShopStoreAddress2(String shopStoreAddress2) {
		this.shopStoreAddress2 = shopStoreAddress2;
	}
	public boolean isEnabled() {
		return enabled;
	}
	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}
	
	
}
