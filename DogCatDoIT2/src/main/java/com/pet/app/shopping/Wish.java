package com.pet.app.shopping;

public class Wish {
	private long wishIdx;
	private long userIdx;
	private long detailId;
	private String wish_date;
	public long getWishIdx() {
		return wishIdx;
	}
	public void setWishIdx(long wishIdx) {
		this.wishIdx = wishIdx;
	}
	public long getUserIdx() {
		return userIdx;
	}
	public void setUserIdx(long userIdx) {
		this.userIdx = userIdx;
	}
	public long getDetailId() {
		return detailId;
	}
	public void setDetailId(long detailId) {
		this.detailId = detailId;
	}
	public String getWish_date() {
		return wish_date;
	}
	public void setWish_date(String wish_date) {
		this.wish_date = wish_date;
	}
	
	
}
